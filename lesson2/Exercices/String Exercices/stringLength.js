// ==========================================
// PROPIEDAD .LENGTH (LONGITUD DE CADENA)
// ==========================================

let string = "Hello World";

// La propiedad .length devuelve el número total de caracteres en la cadena.
// Esto incluye letras, números, símbolos y espacios en blanco.
// "Hello World" tiene 5 letras + 1 espacio + 5 letras = 11 caracteres.

console.log(string.length); // 11