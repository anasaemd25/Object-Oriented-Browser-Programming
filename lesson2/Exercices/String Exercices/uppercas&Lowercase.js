// MAYÚSCULAS Y MINÚSCULAS
// ==========================================

let str1 = "Convert to Uppercase & LowerCase";

// .toUpperCase(): Convierte toda la cadena a MAYÚSCULAS.
// No modifica la original, devuelve una nueva.
let strUpperCase = str1.toUpperCase();

// .toLowerCase(): Convierte toda la cadena a minúsculas.
let strLowerCase = str1.toLowerCase();

console.log(strUpperCase); // "CONVERT TO UPPERCASE & LOWERCASE"
console.log(strLowerCase); // "convert to uppercase & lowercase"
